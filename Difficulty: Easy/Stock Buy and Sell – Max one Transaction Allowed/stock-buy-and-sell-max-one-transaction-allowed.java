class Solution {
    public int maximumProfit(int prices[]) {
        // Code here
        int max=0;
        int sell=0;
        for(int i=prices.length-1;i>=0;i--){
            max=Math.max(max,prices[i]);
            sell=Math.max(max-prices[i],sell);
        }
        if(sell>0){
            return sell;
        }
        return 0;
    }
}